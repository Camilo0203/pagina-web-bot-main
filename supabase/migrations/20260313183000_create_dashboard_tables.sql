create or replace function public.save_guild_config(
  p_guild_id text,
  p_general_settings jsonb,
  p_moderation_settings jsonb,
  p_dashboard_preferences jsonb
)
returns setof public.guild_configs
language plpgsql
as $$
declare
  v_user_id uuid := auth.uid();
begin
  if v_user_id is null then
    raise exception 'Authentication required';
  end if;

  if not public.is_manageable_guild(p_guild_id) then
    raise exception 'You do not have access to this guild';
  end if;

  insert into public.guild_configs (
    guild_id,
    general_settings,
    moderation_settings,
    dashboard_preferences,
    updated_by
  )
  values (
    p_guild_id,
    coalesce(p_general_settings, '{}'::jsonb),
    coalesce(p_moderation_settings, '{}'::jsonb),
    coalesce(p_dashboard_preferences, '{}'::jsonb),
    v_user_id
  )
  on conflict (guild_id) do update
  set
    general_settings = excluded.general_settings,
    moderation_settings = excluded.moderation_settings,
    dashboard_preferences = excluded.dashboard_preferences,
    updated_by = excluded.updated_by,
    updated_at = now();

  insert into public.guild_dashboard_events (
    guild_id,
    actor_user_id,
    event_type,
    title,
    description,
    metadata
  )
  values (
    p_guild_id,
    v_user_id,
    'config_saved',
    'Configuracion actualizada',
    'Se guardaron ajustes generales, moderacion y preferencias del panel.',
    jsonb_build_object(
      'general', true,
      'moderation', true,
      'preferences', true
    )
  );

  return query
  select *
  from public.guild_configs
  where guild_id = p_guild_id;
end;
$$;
