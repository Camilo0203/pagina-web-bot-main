/*
  # Dashboard tables and policies

  1. New tables
    - `bot_guilds`
    - `user_guild_access`
    - `guild_configs`
    - `guild_dashboard_events`
    - `guild_metrics_daily`

  2. Security
    - RLS for dashboard-facing tables
    - helper function `is_manageable_guild`
    - RPC `save_guild_config` for audited writes

  3. Notes
    - `bot_stats` remains public for the landing page
    - `bot_guilds` is maintained by the bot/backend using service role credentials
*/

create extension if not exists pgcrypto;

create or replace function public.set_updated_at_timestamp()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

create table if not exists public.bot_guilds (
  guild_id text primary key,
  guild_name text not null,
  guild_icon text,
  member_count bigint default 0,
  premium_tier text default 'free',
  setup_completed boolean default false,
  last_heartbeat_at timestamptz default now(),
  created_at timestamptz default now(),
  updated_at timestamptz default now()
);

create table if not exists public.user_guild_access (
  id uuid primary key default gen_random_uuid(),
  user_id uuid not null references auth.users(id) on delete cascade,
  guild_id text not null,
  guild_name text not null,
  guild_icon text,
  permissions_raw text not null default '0',
  can_manage boolean not null default false,
  is_owner boolean not null default false,
  bot_installed boolean not null default false,
  member_count bigint,
  premium_tier text,
  bot_last_seen_at timestamptz,
  last_synced_at timestamptz not null default now(),
  created_at timestamptz default now(),
  updated_at timestamptz default now(),
  unique (user_id, guild_id)
);

create table if not exists public.guild_configs (
  guild_id text primary key references public.bot_guilds(guild_id) on delete cascade,
  general_settings jsonb not null default '{}'::jsonb,
  moderation_settings jsonb not null default '{}'::jsonb,
  dashboard_preferences jsonb not null default '{}'::jsonb,
  updated_by uuid references auth.users(id) on delete set null,
  created_at timestamptz default now(),
  updated_at timestamptz default now()
);

create table if not exists public.guild_dashboard_events (
  id uuid primary key default gen_random_uuid(),
  guild_id text not null references public.bot_guilds(guild_id) on delete cascade,
  actor_user_id uuid references auth.users(id) on delete set null,
  event_type text not null,
  title text not null,
  description text not null,
  metadata jsonb not null default '{}'::jsonb,
  created_at timestamptz default now()
);

create table if not exists public.guild_metrics_daily (
  id bigint generated by default as identity primary key,
  guild_id text not null references public.bot_guilds(guild_id) on delete cascade,
  metric_date date not null default current_date,
  commands_executed bigint not null default 0,
  moderated_messages bigint not null default 0,
  active_members bigint not null default 0,
  uptime_percentage numeric(5,2) not null default 100,
  created_at timestamptz default now(),
  updated_at timestamptz default now(),
  unique (guild_id, metric_date)
);

