drop policy if exists "Users can read own guild access" on public.user_guild_access;
create policy "Users can read own guild access"
  on public.user_guild_access
  for select
  to authenticated
  using (user_id = auth.uid());

drop policy if exists "Users can read manageable guild configs" on public.guild_configs;
create policy "Users can read manageable guild configs"
  on public.guild_configs
  for select
  to authenticated
  using (public.is_manageable_guild(guild_id));

drop policy if exists "Users can insert manageable guild configs" on public.guild_configs;
create policy "Users can insert manageable guild configs"
  on public.guild_configs
  for insert
  to authenticated
  with check (public.is_manageable_guild(guild_id));

drop policy if exists "Users can update manageable guild configs" on public.guild_configs;
create policy "Users can update manageable guild configs"
  on public.guild_configs
  for update
  to authenticated
  using (public.is_manageable_guild(guild_id))
  with check (public.is_manageable_guild(guild_id));

drop policy if exists "Users can read manageable dashboard events" on public.guild_dashboard_events;
create policy "Users can read manageable dashboard events"
  on public.guild_dashboard_events
